from __future__ import annotations

from dataclasses import dataclass, field

from driver.contracts import PatchAction
from repair.strategies.llm import LLMStrategy, LLMStrategyInput, LLMStrategyOutput


@dataclass
class MockLLMStrategy(LLMStrategy):
    static_actions: list[PatchAction] = field(default_factory=list)
    static_rationale: str = "Mock LLM strategy output."
    static_diff_summary: str = "Mock diff summary."

    def propose_with_schema(self, payload: LLMStrategyInput) -> LLMStrategyOutput:
        can_apply = len(self.static_actions) > 0
        rationale = f"{self.static_rationale} error={payload.error.category} run={payload.run_id}"
        return LLMStrategyOutput(
            can_apply=can_apply,
            rationale=rationale,
            diff_summary=self.static_diff_summary if can_apply else "No patch generated by mock LLM strategy.",
            actions=list(self.static_actions),
        )
